	public List<Coppia> getCoppie(double rank, Map <Integer, Movie> mappaMovie) {
		String sql = "SELECT m1.id AS id1,m2.id AS id2,COUNT( distinct r1.actor_id) AS attori_distinti "
				+ "FROM movies m1, movies m2, roles r1, roles r2 "
				+ "WHERE m1.id=r1.movie_id AND m2.id=r2.movie_id AND r1.actor_id=r2.actor_id AND m1.id>m2.id AND m1.`rank`>? AND m2.`rank`>? "
				+ "GROUP BY m1.id,m2.id";
		List<Coppia> result = new ArrayList<Coppia>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setDouble(1, rank);
			st.setDouble(2, rank);
			ResultSet res = st.executeQuery();
			
			while (res.next()) {

				Coppia coppia=new Coppia(mappaMovie.get(res.getInt("id1")),mappaMovie.get(res.getInt("id2")),res.getInt("attori_distinti"));
				
				result.add(coppia);
			}
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
		
	
	}
	
	
	
	
	
	
	
